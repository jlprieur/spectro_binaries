      SUBROUTINEVITESSE_CALCULEE_BS3(PHI,PHI3,VIC1,VIC2,VV,VV3,EE,EE3,OM
     +EGA,OMEGA3,K1,K2,K3,VZERO)
      IMPLICITNONE
      DOUBLEPRECISIONPHI,PHI3
      REALM,PI,EMME,ANOM,ANOM_OLD,VV,VV3
      REALEE,EE3,OMEGA,OMEGA3,K1,K2,K3,VZERO,VIC1,VIC2
      INTEGERI
      PI=3.1415926535
      M=2.*PI*PHI
      EMME=AMOD(M,2.*PI)
      ANOM_OLD=EMME
      DO80020I=1,1000
      ANOM=EMME+EE*SIN(ANOM_OLD)
      IF(ABS(ANOM-ANOM_OLD).LT.1.E-6)GOTO12
      ANOM_OLD=ANOM
80020 CONTINUE
   12 VV=2.*ATAN(TAN(ANOM/2.)*SQRT((1+EE)/(1-EE)))
      M=2.*PI*PHI3
      EMME=AMOD(M,2.*PI)
      ANOM_OLD=EMME
      DO80021I=1,1000
      ANOM=EMME+EE3*SIN(ANOM_OLD)
      IF(ABS(ANOM-ANOM_OLD).LT.1.E-6)GOTO14
      ANOM_OLD=ANOM
80021 CONTINUE
   14 VV3=2.*ATAN(TAN(ANOM/2.)*SQRT((1+EE3)/(1-EE3)))
      VIC1=VZERO+K1*(EE*COS(OMEGA)+COS(VV+OMEGA))+K3*(EE3*COS(OMEGA3)+CO
     +S(VV3+OMEGA3))
      VIC2=VZERO-K2*(EE*COS(OMEGA)+COS(VV+OMEGA))+K3*(EE3*COS(OMEGA3)+CO
     +S(VV3+OMEGA3))
      RETURN
      END
